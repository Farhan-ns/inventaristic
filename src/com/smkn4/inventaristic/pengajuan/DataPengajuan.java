    private void cmdHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdHapusActionPerformed
         String idWhoWantToBeDelete = tbl_data.getValueAt(baris, 1).toString();
        //(baris, 1) --> 1 itu menunjukan kolom (nis), indexnya seperti array
        try {
            Statement stmt = koneksi.createStatement();
            String query = "DELETE FROM pengajuan_barang WHERE id_permintaan = '" + idWhoWantToBeDelete + "'";
            int berhasil = stmt.executeUpdate(query);
            if (berhasil == 1) {
                JOptionPane.showMessageDialog(null, "Data Berhasil Dihapus");
                dtm.getDataVector().removeAllElements();
                showData();
            } else {
                JOptionPane.showMessageDialog(null, "Data Gagal Dihapus");
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_cmdHapusActionPerformed

