    public void scan(){
        String nis = txt_barcode.getText();
        txt_barcode.setText("");
        if(act == "pinjam"){
            peminjaman(nis);
        }else if(act == "minta"){
            permintaan(nis);
        }else if(act == "kembali"){
            pengembalian(nis);
        }
    }
    
    public void peminjaman(String nis) {
        peminjamanBarang pinjam = new peminjamanBarang();
        pinjam.setVisible(true);
        pinjam.showIdentitas(nis);
        this.dispose();
    }
    
    public void permintaan(String nis) {
        permintaanBarang minta = new permintaanBarang();
        minta.setVisible(true);
        minta.showIdentitas(nis);
        this.dispose();
    }
    
    public void pengembalian(String nis) {
        pengembalianBarang kembali = new pengembalianBarang();
        kembali.setVisible(true);
        kembali.showIdentitas(nis);
        this.dispose();
    }
